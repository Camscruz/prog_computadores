# Escreva um programa que leia 2 números inteiros a e b e informe se a é divisível por b

a=int(input("Informe um número inteiro a: "))
b=int(input("Informe um número inteiro b: "))

if (a%b==0):
    print ("O número a é divisível por b.")
else: 
    print ("O número a não é divisível por b.")
